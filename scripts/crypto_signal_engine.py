class SignalGenerator:
    """Generates trading signals based on market structure and flow."""

    def __init__(self):
        self.structure_analyzer = MarketStructureAnalyzer()
        self.flow_analyzer = OrderFlowAnalyzer()
        # Advanced order flow scorer (from order_flow_engine.py)
        if ADVANCED_ORDER_FLOW:
            self.of_scorer = OrderFlowScorer()
            logger.info("Advanced OrderFlowScorer loaded — OHLCV PROXY mode")
        else:
            self.of_scorer = None
            logger.warning("Advanced OrderFlowScorer NOT available — running basic flow only")
    
    def generate_signals(
        self,
        symbol: str,
        timeframe: str,
        df: pd.DataFrame,
        strategy_name: str
    ) -> List[Signal]:
        """Generate trading signals for a given symbol and timeframe."""
        signals = []
        
        if len(df) < 30:
            return signals
        
        # Detect market structure
        swings = self.structure_analyzer.detect_swing_points(df, lookback=5)
        bos_choch = self.structure_analyzer.detect_bos_choch(swings, df) or {}
        order_blocks = self.structure_analyzer.detect_order_blocks(df) or []
        liquidity = self.structure_analyzer.detect_liquidity_sweeps(df, swings) or []

        # Analyze order flow
        absorption = self.flow_analyzer.detect_absorption(df) or {}
        imbalance = self.flow_analyzer.detect_imbalance(df) or {}

        # Null-safe: ensure nested dicts are not None
        if bos_choch.get('bos') is None:
            bos_choch['bos'] = {}
        if bos_choch.get('choch') is None:
            bos_choch['choch'] = {}

        current_price = df['close'].iloc[-1]
        current_time = pd.Timestamp(df.index[-1])

        bos_dir = bos_choch.get('bos', {}).get('direction')
        bos_strength = bos_choch.get('bos', {}).get('strength', 0)
        choch_dir = bos_choch.get('choch', {}).get('direction')

        # Log BOS state for debugging
        logger.info(f"SCAN {symbol} {timeframe}: BOS={bos_dir}(str={bos_strength:.0f}) "
                     f"CHOCH={choch_dir} OBs={len(order_blocks)} "
                     f"imbalance={imbalance.get('direction')} swings={len(swings)}")

        # Signal 1: OB_Retest_Long (Strong)
        # Conditions: Price retests bullish OB + BOS up + flow confirmation
        for ob in order_blocks:
            if ob.type == 'bullish' and current_price >= ob.low and current_price <= ob.high:
                if bos_dir == 'up':
                    # Strong signal: BOS + OB + flow all align
                    if imbalance.get('direction') == 'bullish':
                        confidence = (
                            ob.strength * 0.25 +
                            bos_strength * 0.30 +
                            imbalance.get('strength', 0) * 0.25 +
                            (80 if absorption.get('type') == 'bullish' else 40) * 0.20
                        )

                        if confidence > 55:
                            signals.append(self._create_signal(
                                symbol=symbol, timeframe=timeframe,
                                strategy_name=strategy_name, direction='LONG',
                                entry_price=current_price,
                                stop_loss=ob.low * 0.99,
                                take_profit=current_price * 1.03,
                                confidence_score=confidence,
                                order_blocks_used=[ob],
                                flow_confirmation={
                                    'imbalance': imbalance.get('direction'),
                                    'absorption': absorption.get('type'),
                                    'bos': bos_choch.get('bos')
                                },
                                timestamp=current_time
                            ))
                    # Moderate signal: BOS + OB (no flow required if BOS strong)
                    elif bos_strength > 70:
                        confidence = ob.strength * 0.4 + bos_strength * 0.6
                        if confidence > 60:
                            signals.append(self._create_signal(
                                symbol=symbol, timeframe=timeframe,
                                strategy_name=strategy_name, direction='LONG',
                                entry_price=current_price,
                                stop_loss=ob.low * 0.99,
                                take_profit=current_price * 1.025,
                                confidence_score=confidence * 0.9,
                                order_blocks_used=[ob],
                                flow_confirmation={
                                    'bos': bos_choch.get('bos'),
                                    'note': 'strong_bos_no_flow'
                                },
                                timestamp=current_time
                            ))

        # Signal 2: OB_Retest_Short (Strong)
        for ob in order_blocks:
            if ob.type == 'bearish' and current_price >= ob.low and current_price <= ob.high:
                if bos_dir == 'down':
                    if imbalance.get('direction') == 'bearish':
                        confidence = (
                            ob.strength * 0.25 +
                            bos_strength * 0.30 +
                            imbalance.get('strength', 0) * 0.25 +
                            (80 if absorption.get('type') == 'bearish' else 40) * 0.20
                        )

                        if confidence > 55:
                            signals.append(self._create_signal(
                                symbol=symbol, timeframe=timeframe,
                                strategy_name=strategy_name, direction='SHORT',
                                entry_price=current_price,
                                stop_loss=ob.high * 1.01,
                                take_profit=current_price * 0.97,
                                confidence_score=confidence,
                                order_blocks_used=[ob],
                                flow_confirmation={
                                    'imbalance': imbalance.get('direction'),
                                    'absorption': absorption.get('type'),
                                    'bos': bos_choch.get('bos')
                                },
                                timestamp=current_time
                            ))
                    elif bos_strength > 70:
                        confidence = ob.strength * 0.4 + bos_strength * 0.6
                        if confidence > 60:
                            signals.append(self._create_signal(
                                symbol=symbol, timeframe=timeframe,
                                strategy_name=strategy_name, direction='SHORT',
                                entry_price=current_price,
                                stop_loss=ob.high * 1.01,
                                take_profit=current_price * 0.975,
                                confidence_score=confidence * 0.9,
                                order_blocks_used=[ob],
                                flow_confirmation={
                                    'bos': bos_choch.get('bos'),
                                    'note': 'strong_bos_no_flow'
                                },
                                timestamp=current_time
                            ))
        
        # ── Advanced Order Flow Gate ──────────────────────────────
        # Compute order flow score for each candidate signal and reject
        # those below the threshold.  Attach full analysis to every signal.
        if self.of_scorer is not None and TRADING_CONFIG.get('order_flow_enabled'):
            swing_dicts = [
                {'index': s.timestamp, 'price': s.price, 'type': s.type}
                for s in swings
            ]
            gated = []
            threshold = TRADING_CONFIG.get('order_flow_threshold_paper', 60)
            for sig in signals:
                direction_arg = 'long' if sig.direction == 'LONG' else 'short'
                of_result = self.of_scorer.score(
                    df, direction=direction_arg,
                    swings=swing_dicts, lookback=20
                )
                # Attach order flow data to signal
                sig.flow_confirmation['order_flow_score'] = of_result.total_score
                sig.flow_confirmation['order_flow_strength'] = of_result.strength.value
                sig.flow_confirmation['order_flow_bias'] = of_result.direction_bias
                sig.flow_confirmation['order_flow_breakdown'] = {
                    'delta': of_result.delta_score,
                    'volume_spike': of_result.volume_spike_score,
                    'absorption': of_result.absorption_score,
                    'imbalance': of_result.imbalance_score,
                    'sweep_trapped': of_result.sweep_trapped_score,
                }
                sig.flow_confirmation['order_flow_confirmations'] = of_result.confirmations
                sig.flow_confirmation['order_flow_warnings'] = of_result.warnings
                sig.flow_confirmation['data_type'] = 'OHLCV_PROXY'

                if of_result.total_score >= threshold:
                    # Boost confidence by blending structure + flow
                    flow_bonus = (of_result.total_score - threshold) * 0.3
                    sig.confidence_score = min(100, sig.confidence_score + flow_bonus)
                    gated.append(sig)
                    logger.info(
                        f"OF-PASS {symbol} {sig.direction}: "
                        f"score={of_result.total_score:.0f} "
                        f"({of_result.strength.value}) "
                        f"confirms={of_result.confirmations}"
                    )
                else:
                    logger.info(
                        f"OF-REJECT {symbol} {sig.direction}: "
                        f"score={of_result.total_score:.0f} < {threshold} "
                        f"warns={of_result.warnings}"
                    )
            signals = gated

        # Signal 3: Breakout_Retest
        # Price breaks support/resistance and comes back to retest
        if len(swings) >= 2:
            last_swing = swings[-1]
            prev_price = df['close'].iloc[-2]
            
            if last_swing.type == 'low' and prev_price > last_swing.price and current_price < last_swing.price:
                # Broke below support (downside breakout)
                if imbalance.get('direction') == 'bearish':
                    confidence = 50 + imbalance.get('strength', 0) * 0.5
                    signals.append(self._create_signal(
                        symbol=symbol,
                        timeframe=timeframe,
                        strategy_name=strategy_name,
                        direction='SHORT',
                        entry_price=current_price,
                        stop_loss=last_swing.price * 1.01,
                        take_profit=current_price * 0.97,
                        confidence_score=confidence,
                        order_blocks_used=[],
                        flow_confirmation={'breakout': 'down', 'imbalance': imbalance},
                        timestamp=current_time
                    ))
            
            elif last_swing.type == 'high' and prev_price < last_swing.price and current_price > last_swing.price:
                # Broke above resistance (upside breakout)
                if imbalance.get('direction') == 'bullish':
                    confidence = 50 + imbalance.get('strength', 0) * 0.5
                    signals.append(self._create_signal(
                        symbol=symbol,
                        timeframe=timeframe,
                        strategy_name=strategy_name,
                        direction='LONG',
                        entry_price=current_price,
                        stop_loss=last_swing.price * 0.99,
                        take_profit=current_price * 1.03,
                        confidence_score=confidence,
                        order_blocks_used=[],
                        flow_confirmation={'breakout': 'up', 'imbalance': imbalance},
                        timestamp=current_time
                    ))
        
        return signals
    
    def _create_signal(
        self,
        symbol: str,
        timeframe: str,
        strategy_name: str,
        direction: str,
        entry_price: float,
        stop_loss: float,
        take_profit: float,
        confidence_score: float,
        order_blocks_used: List[OrderBlock],
        flow_confirmation: Dict[str, Any],
        timestamp: pd.Timestamp
    ) -> Signal:
        """Helper to create a Signal object."""
        risk = abs(entry_price - stop_loss)
        reward = abs(take_profit - entry_price)
        
        # Risk per trade is 1% of capital
        capital = TRADING_CONFIG['starting_capital']
        risk_amount = capital * TRADING_CONFIG['risk_per_trade']
        position_size = risk_amount / risk if risk > 0 else 0
        
        return Signal(
            timestamp=pd.Timestamp(timestamp).to_pydatetime(),
            symbol=symbol,
            timeframe=timeframe,
            strategy_name=strategy_name,
            direction=direction,
            entry_price=entry_price,
            stop_loss=stop_loss,
            take_profit=take_profit,
            confidence_score=min(100, max(0, confidence_score)),
            risk_amount=risk_amount,
            position_size=position_size,
            invalidation_level=stop_loss,
            order_blocks_used=order_blocks_used,
            flow_confirmation=flow_confirmation
        )

