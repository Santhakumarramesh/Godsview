# ORDER FLOW ANALYSIS (OHLCV-Based Proxy)
# ============================================================================

class OrderFlowAnalyzer:
    """Analyzes order flow using OHLCV-based proxies."""
    
    @staticmethod
    def calculate_clv(df: pd.DataFrame) -> pd.Series:
        """
        Close Location Value (CLV).
        How close the close is relative to high-low range.
        CLV = ((C - L) - (H - C)) / (H - L)
        """
        numerator = (df['close'] - df['low']) - (df['high'] - df['close'])
        denominator = df['high'] - df['low']
        denominator = denominator.replace(0, 1)  # Avoid division by zero
        return numerator / denominator
    
    @staticmethod
    def calculate_delta_proxy(df: pd.DataFrame) -> pd.Series:
        """
        Delta proxy using volume and close position.
        Positive close at high suggests buying pressure.
        Negative close at low suggests selling pressure.
        """
        clv = OrderFlowAnalyzer.calculate_clv(df)
        return df['volume'] * clv
    
    @staticmethod
    def calculate_cvd(df: pd.DataFrame) -> pd.Series:
        """
        Cumulative Volume Delta (CVD).
        Sum of delta proxies over time.
        """
        delta = OrderFlowAnalyzer.calculate_delta_proxy(df)
        return delta.cumsum()
    
    @staticmethod
    def detect_absorption(df: pd.DataFrame, lookback: int = 20) -> Dict[str, Any]:
        """
        Detect absorption (institutional buying/selling).
        
        Absorption occurs when price doesn't move significantly despite
        large volume (delta proxy).
        """
        if len(df) < lookback:
            return {'absorption': False, 'type': None, 'strength': 0}
        
        recent = df.tail(lookback)
        price_range = (recent['high'].max() - recent['low'].min()) / recent['close'].iloc[0]
        volume_avg = recent['volume'].mean()
        current_volume = recent['volume'].iloc[-1]
        
        # Strong absorption: high volume but low price movement
        if current_volume > volume_avg * 1.5 and price_range < 0.01:  # Less than 1% range
            delta = OrderFlowAnalyzer.calculate_delta_proxy(recent)
            if abs(delta.iloc[-1]) > volume_avg * 0.7:
                absorption_type = 'bullish' if delta.iloc[-1] > 0 else 'bearish'
                return {
                    'absorption': True,
                    'type': absorption_type,
                    'strength': min(100, abs(delta.iloc[-1]) / volume_avg * 100)
                }
        
        return {'absorption': False, 'type': None, 'strength': 0}
    
    @staticmethod
    def detect_imbalance(df: pd.DataFrame, lookback: int = 5) -> Dict[str, Any]:
        """
        Detect volume imbalances (strong directional pressure).
        """
        if len(df) < lookback:
            return {'imbalance': False, 'direction': None}
        
        recent = df.tail(lookback)
        delta = OrderFlowAnalyzer.calculate_delta_proxy(recent)
        cvd = delta.cumsum()
        
        imbalance_strength = abs(cvd.iloc[-1]) / (recent['volume'].sum() + 1)
        
        if imbalance_strength > 0.3:  # >30% imbalance
            direction = 'bullish' if cvd.iloc[-1] > 0 else 'bearish'
            return {
                'imbalance': True,
                'direction': direction,
                'strength': min(100, imbalance_strength * 100)
            }
        
        return {'imbalance': False, 'direction': None}

