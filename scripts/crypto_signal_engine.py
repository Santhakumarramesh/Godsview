class PaperTradingEngine:
    """Simulates trade execution in paper mode."""
    
    def __init__(self):
        self.positions = []
        self.position_id_counter = 0
        self.starting_equity = TRADING_CONFIG['starting_capital']
        self.current_equity = self.starting_equity
        self.equity_history = [self.starting_equity]
        self.risk_gate = RiskGate()
        self.performance = defaultdict(lambda: {
            'trades': 0,
            'wins': 0,
            'losses': 0,
            'profit_factor': 0,
            'win_rate': 0,
            'max_drawdown': 0,
            'total_pnl': 0
        })
    
    def execute_signal(self, signal: Signal) -> Optional[PaperPosition]:
        """
        Execute a signal as a paper trade.
        
        Returns: PaperPosition if opened, None if rejected
        """
        can_trade, reason = self.risk_gate.can_trade(signal, self.positions, self.current_equity)
        
        if not can_trade:
            alert = {
                'timestamp': signal.timestamp.isoformat(),
                'type': 'signal_rejected',
                'symbol': signal.symbol,
                'strategy': signal.strategy_name,
                'reason': reason
            }
            self._write_alert(alert)
            logger.info(f"PAPER MODE - Signal rejected: {signal.symbol} {signal.direction} - {reason}")
            return None
        
        # Create paper position
        self.position_id_counter += 1
        position = PaperPosition(
            position_id=f"POS_{self.position_id_counter:05d}",
            symbol=signal.symbol,
            timeframe=signal.timeframe,
            strategy_name=signal.strategy_name,
            direction=signal.direction,
            entry_price=signal.entry_price,
            entry_time=signal.timestamp,
            stop_loss=signal.stop_loss,
            take_profit=signal.take_profit,
            position_size=signal.position_size,
            risk_amount=signal.risk_amount,
            pnl=0,
            pnl_pct=0,
            status='open',
            close_time=None,
            close_price=None,
            close_reason='',
            candles_held=0
        )
        
        self.positions.append(position)
        
        # Log trade opening
        alert = {
            'timestamp': signal.timestamp.isoformat(),
            'type': 'trade_opened',
            'position_id': position.position_id,
            'symbol': signal.symbol,
            'timeframe': signal.timeframe,
            'strategy': signal.strategy_name,
            'direction': signal.direction,
            'entry_price': signal.entry_price,
            'stop_loss': signal.stop_loss,
            'take_profit': signal.take_profit,
            'position_size': signal.position_size,
            'confidence_score': signal.confidence_score,
        }
        self._write_alert(alert)
        logger.info(f"PAPER MODE - Trade opened: {position.position_id} "
                   f"{signal.symbol} {signal.direction} @ {signal.entry_price:.2f}")
        
        return position
    
    def update_positions(self, price_updates: Dict[str, float]) -> List[PaperPosition]:
        """
        Update open positions with latest prices.
        Check for SL/TP hits.
        
        price_updates: {symbol: current_price}
        """
        closed_positions = []
        
        for position in self.positions:
            if position.status != 'open':
                continue
            
            if position.symbol not in price_updates:
                continue
            
            current_price = price_updates[position.symbol]
            position.candles_held += 1
            
            # Check stop loss
            if position.direction == 'LONG' and current_price <= position.stop_loss:
                self._close_position(position, current_price, 'SL', 'closed_sl')
                closed_positions.append(position)
                continue
            
            if position.direction == 'SHORT' and current_price >= position.stop_loss:
                self._close_position(position, current_price, 'SL', 'closed_sl')
                closed_positions.append(position)
                continue
            
            # Check take profit
            if position.direction == 'LONG' and current_price >= position.take_profit:
                self._close_position(position, current_price, 'TP', 'closed_tp')
                closed_positions.append(position)
                continue
            
            if position.direction == 'SHORT' and current_price <= position.take_profit:
                self._close_position(position, current_price, 'TP', 'closed_tp')
                closed_positions.append(position)
                continue
            
            # Time stop: close after N candles
            time_stop_candles = 50
            if position.candles_held >= time_stop_candles:
                self._close_position(position, current_price, 'TIME', 'closed_time')
                closed_positions.append(position)
        
        # Update equity
        self._update_equity()
        
        return closed_positions
    
    def _close_position(
        self,
        position: PaperPosition,
        close_price: float,
        reason_short: str,  # 'SL', 'TP', 'TIME'
        status: str
    ):
        """Close a position and calculate PnL."""
        position.close_price = close_price
        position.close_time = datetime.now()
        position.status = status
        
        if position.direction == 'LONG':
            pnl = (close_price - position.entry_price) * position.position_size
        else:  # SHORT
            pnl = (position.entry_price - close_price) * position.position_size
        
        position.pnl = pnl
        position.pnl_pct = (pnl / position.risk_amount * 100) if position.risk_amount > 0 else 0
        
        # Record for circuit breaker
        self.risk_gate.record_trade_result(position)
        
        # Update performance
        self.performance[position.strategy_name]['trades'] += 1
        if pnl > 0:
            self.performance[position.strategy_name]['wins'] += 1
        else:
            self.performance[position.strategy_name]['losses'] += 1
        
        # Log closure
        alert = {
            'timestamp': position.close_time.isoformat(),
            'type': f'trade_closed_{reason_short.lower()}',
            'position_id': position.position_id,
            'symbol': position.symbol,
            'strategy': position.strategy_name,
            'direction': position.direction,
            'entry_price': position.entry_price,
            'close_price': close_price,
            'pnl': pnl,
            'pnl_pct': position.pnl_pct,
            'candles_held': position.candles_held,
        }
        self._write_alert(alert)
        logger.info(f"PAPER MODE - Trade closed {reason_short}: {position.position_id} "
                   f"{position.symbol} PnL: ${pnl:.2f} ({position.pnl_pct:.1f}%)")
    
    def _update_equity(self):
        """Calculate current equity from open and closed positions."""
        equity = self.starting_equity
        
        # Add PnL from closed positions
        for position in self.positions:
            if position.status.startswith('closed'):
                equity += position.pnl
        
        self.current_equity = equity
        self.equity_history.append(equity)
        self.risk_gate.update_drawdown(equity)
    
    def _write_alert(self, alert: Dict[str, Any]):
        """Write alert to alerts.jsonl"""
        alert_file = ALERT_DIRS['alerts'] / 'alerts.jsonl'
        with open(alert_file, 'a') as f:
            f.write(json.dumps(alert) + '\n')
    
    def save_positions(self):
        """Save current positions to JSON."""
        positions_data = []
        for pos in self.positions:
            positions_data.append({
                'position_id': pos.position_id,
                'symbol': pos.symbol,
                'timeframe': pos.timeframe,
                'strategy_name': pos.strategy_name,
                'direction': pos.direction,
                'entry_price': pos.entry_price,
                'entry_time': pos.entry_time.isoformat(),
                'stop_loss': pos.stop_loss,
                'take_profit': pos.take_profit,
                'position_size': pos.position_size,
                'status': pos.status,
                'pnl': pos.pnl,
                'pnl_pct': pos.pnl_pct,
            })
        
        positions_file = ALERT_DIRS['positions'] / 'positions.json'
        with open(positions_file, 'w') as f:
            json.dump(positions_data, f, indent=2)
    
    def save_performance(self):
        """Save performance metrics to JSON."""
        perf_data = {}
        for strategy, metrics in self.performance.items():
            trades = metrics['trades']
            wins = metrics['wins']
            
            # Calculate metrics
            win_rate = (wins / trades * 100) if trades > 0 else 0
            
            perf_data[strategy] = {
                'trades': trades,
                'wins': wins,
                'losses': metrics['losses'],
                'win_rate': win_rate,
                'max_drawdown': metrics['max_drawdown'],
                'total_pnl': metrics['total_pnl'],
            }
        
        perf_file = ALERT_DIRS['performance'] / 'performance.json'
        with open(perf_file, 'w') as f:
            json.dump(perf_data, f, indent=2)

