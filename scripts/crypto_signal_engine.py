class RiskGate:
    """Enforces risk management rules before executing trades."""
    
    def __init__(self):
        self.daily_loss = 0
        self.daily_start_equity = TRADING_CONFIG['starting_capital']
        self.max_drawdown = 0
        self.consecutive_losses = defaultdict(int)
        self.paused_strategies = {}  # strategy_name -> resume_time
    
    def can_trade(
        self,
        signal: Signal,
        current_positions: List[PaperPosition],
        current_equity: float
    ) -> Tuple[bool, str]:
        """
        Check if signal passes risk gate.
        
        Returns: (can_trade, reason)
        """
        # Check 1: Max concurrent positions
        open_positions = [p for p in current_positions if p.status == 'open']
        if len(open_positions) >= TRADING_CONFIG['max_concurrent_positions']:
            return False, f"Max concurrent positions ({TRADING_CONFIG['max_concurrent_positions']}) reached"
        
        # Check 2: Max 1 position per symbol
        symbol_positions = [p for p in open_positions if p.symbol == signal.symbol]
        if len(symbol_positions) >= TRADING_CONFIG['max_positions_per_symbol']:
            return False, f"Max positions for {signal.symbol} already open"
        
        # Check 3: Daily loss limit
        capital = TRADING_CONFIG['starting_capital']
        daily_loss_limit = capital * (TRADING_CONFIG['max_daily_loss_pct'] / 100)
        if self.daily_loss > daily_loss_limit:
            return False, f"Daily loss limit (${daily_loss_limit:.2f}) exceeded"
        
        # Check 4: Drawdown limit
        drawdown = (self.daily_start_equity - current_equity) / self.daily_start_equity
        dd_limit = TRADING_CONFIG['max_drawdown_pct'] / 100
        if drawdown > dd_limit:
            return False, f"Drawdown limit ({TRADING_CONFIG['max_drawdown_pct']}%) exceeded"
        
        # Check 5: Strategy paused?
        now = time.time()
        if signal.strategy_name in self.paused_strategies:
            resume_time = self.paused_strategies[signal.strategy_name]
            if now < resume_time:
                return False, f"Strategy paused until {datetime.fromtimestamp(resume_time)}"
            else:
                # Resume time passed, unpause
                del self.paused_strategies[signal.strategy_name]
        
        # Check 6: Correlation check (simplified)
        # Don't go long on correlated assets simultaneously
        if signal.direction == 'LONG':
            for pos in open_positions:
                if pos.direction == 'SHORT':
                    # Simple correlation check: SOL/ETH are correlated
                    if {signal.symbol, pos.symbol} == {'SOL/USD', 'ETH/USD'}:
                        return False, "Opposite positions on correlated assets blocked"
        
        return True, "APPROVED"
    
    def record_trade_result(self, trade: PaperPosition):
        """Record result of closed trade for circuit breakers."""
        if trade.status in ['closed_sl', 'closed_time']:
            self.consecutive_losses[trade.strategy_name] += 1
            self.daily_loss += abs(trade.pnl)
            
            # Circuit breaker: 3 consecutive losses
            if self.consecutive_losses[trade.strategy_name] >= TRADING_CONFIG['consecutive_loss_threshold']:
                pause_until = time.time() + TRADING_CONFIG['strategy_pause_duration']
                self.paused_strategies[trade.strategy_name] = pause_until
                logger.warning(f"PAPER MODE - Circuit breaker: {trade.strategy_name} paused for 24h. "
                             f"Consecutive losses: {self.consecutive_losses[trade.strategy_name]}")
        else:
            # Reset consecutive loss counter on win
            self.consecutive_losses[trade.strategy_name] = 0
    
    def update_drawdown(self, current_equity: float):
        """Update max drawdown tracking."""
        drawdown = self.daily_start_equity - current_equity
        if drawdown > self.max_drawdown:
            self.max_drawdown = drawdown
    
    def reset_daily(self):
        """Reset daily metrics at end of day."""
        self.daily_loss = 0
        self.daily_start_equity = TRADING_CONFIG['starting_capital']
        self.max_drawdown = 0

