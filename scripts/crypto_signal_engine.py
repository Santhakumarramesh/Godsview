def create_flask_app(trading_engine: PaperTradingEngine, signal_history: deque):
    """Create Flask app for API endpoints."""
    app = Flask(__name__)
    
    @app.route('/health', methods=['GET'])
    def health():
        return jsonify({
            'status': 'healthy',
            'mode': 'PAPER',
            'timestamp': datetime.now().isoformat(),
            'current_equity': trading_engine.current_equity,
            'starting_equity': trading_engine.starting_equity,
        })
    
    @app.route('/strategies', methods=['GET'])
    def strategies():
        strategies_list = []
        for name, config in STRATEGIES_CONFIG.items():
            paused = name in trading_engine.risk_gate.paused_strategies
            strategies_list.append({
                'name': name,
                'symbol': config['symbol'],
                'timeframe': config['timeframe'],
                'enabled': config['enabled'],
                'paused': paused,
                'backtest_pf': config['profit_factor'],
                'backtest_wr': config['win_rate'],
            })
        return jsonify(strategies_list)
    
    @app.route('/signals', methods=['GET'])
    def signals():
        limit = request.args.get('limit', 20, type=int)
        signals_list = [
            {
                'timestamp': s.timestamp.isoformat(),
                'symbol': s.symbol,
                'timeframe': s.timeframe,
                'direction': s.direction,
                'entry_price': s.entry_price,
                'confidence_score': s.confidence_score,
                'strategy': s.strategy_name,
            }
            for s in list(signal_history)[-limit:]
        ]
        return jsonify(signals_list)
    
    @app.route('/positions', methods=['GET'])
    def positions():
        open_positions = [p for p in trading_engine.positions if p.status == 'open']
        positions_list = [
            {
                'position_id': p.position_id,
                'symbol': p.symbol,
                'direction': p.direction,
                'entry_price': p.entry_price,
                'stop_loss': p.stop_loss,
                'take_profit': p.take_profit,
                'entry_time': p.entry_time.isoformat(),
                'candles_held': p.candles_held,
            }
            for p in open_positions
        ]
        return jsonify(positions_list)
    
    @app.route('/performance', methods=['GET'])
    def performance():
        perf = {}
        for strategy, metrics in trading_engine.performance.items():
            trades = metrics['trades']
            wins = metrics['wins']
            perf[strategy] = {
                'trades': trades,
                'wins': wins,
                'losses': metrics['losses'],
                'win_rate': (wins / trades * 100) if trades > 0 else 0,
            }
        return jsonify(perf)
    
    @app.route('/comparison', methods=['GET'])
    def comparison():
        """Compare backtest vs paper results."""
        backtest_data = {
            'SOLUSD_4h': {'pf': 7.33, 'wr': 81.2},
            'ETHUSD_1h': {'pf': 10.58, 'wr': 76.9},
            'BTCUSD_4h': {'pf': 2.02, 'wr': 47.1},
        }
        
        comparison = {}
        for strategy, backtest_metrics in backtest_data.items():
            paper_metrics = trading_engine.performance.get(strategy, {})
            trades = paper_metrics.get('trades', 0)
            
            if trades == 0:
                deviation = None
            else:
                wins = paper_metrics.get('wins', 0)
                paper_wr = (wins / trades * 100) if trades > 0 else 0
                backtest_wr = backtest_metrics['wr']
                deviation = ((paper_wr - backtest_wr) / backtest_wr * 100)
            
            comparison[strategy] = {
                'backtest_wr': backtest_metrics['wr'],
                'paper_wr': (wins / trades * 100) if trades > 0 else 0,
                'deviation_pct': deviation,
                'alert': 'UNDERPERFORMING' if deviation and deviation < -20 else 'NORMAL'
            }
        
        return jsonify(comparison)
    
    @app.route('/alerts', methods=['GET'])
    def alerts():
        limit = request.args.get('limit', 50, type=int)
        alerts_file = ALERT_DIRS['alerts'] / 'alerts.jsonl'
        
        alerts_list = []
        if alerts_file.exists():
            with open(alerts_file, 'r') as f:
                for line in f:
                    try:
                        alerts_list.append(json.loads(line))
                    except:
                        pass
        
        return jsonify(alerts_list[-limit:])
    
    @app.route('/pause/<strategy>', methods=['POST'])
    def pause_strategy(strategy):
        if strategy in trading_engine.risk_gate.paused_strategies:
            return jsonify({'error': 'Already paused'}), 400
        
        pause_until = time.time() + 3600  # 1 hour
        trading_engine.risk_gate.paused_strategies[strategy] = pause_until
        logger.info(f"PAPER MODE - Strategy paused manually: {strategy}")
        return jsonify({'status': 'paused', 'resume_at': datetime.fromtimestamp(pause_until).isoformat()})
    
    @app.route('/resume/<strategy>', methods=['POST'])
    def resume_strategy(strategy):
        if strategy in trading_engine.risk_gate.paused_strategies:
            del trading_engine.risk_gate.paused_strategies[strategy]
        logger.info(f"PAPER MODE - Strategy resumed manually: {strategy}")
        return jsonify({'status': 'resumed'})
    
    @app.route('/kill', methods=['POST'])
    def kill():
        logger.warning("PAPER MODE - Emergency stop initiated")
        # In production, this would signal the main loop to shut down
        return jsonify({'status': 'emergency_stop'})

    @app.route('/inject', methods=['POST'])
    def inject_position():
        """Inject a forced paper position for pipeline validation."""
        try:
            data = request.get_json()
            if not data:
                return jsonify({'error': 'No JSON payload'}), 400

            required = ['symbol', 'direction', 'entry_price', 'stop_loss', 'take_profit']
            missing = [f for f in required if f not in data]
            if missing:
                return jsonify({'error': f'Missing fields: {missing}'}), 400

            import uuid
            pos_id = f"FORCE-{uuid.uuid4().hex[:8]}"
            now = datetime.utcnow()
            price = float(data['entry_price'])
            sl = float(data['stop_loss'])

            position = PaperPosition(
                position_id=pos_id,
                symbol=data['symbol'],
                timeframe=data.get('timeframe', '4h'),
                strategy_name=data.get('strategy_name', 'forced_validation'),
                direction=data['direction'],
                entry_price=price,
                entry_time=now,
                stop_loss=sl,
                take_profit=float(data['take_profit']),
                position_size=float(data.get('position_size', 1000.0)),
                risk_amount=abs(price - sl) * float(data.get('position_size', 1000.0)) / price,
                pnl=0.0,
                pnl_pct=0.0,
                status='open',
                close_time=None,
                close_price=None,
                close_reason='',
                candles_held=0
            )

            trading_engine.positions.append(position)
            logger.info(f"PAPER MODE - Injected forced position: {pos_id} {data['symbol']} {data['direction']}")

            return jsonify({
                'status': 'injected',
                'position_id': pos_id,
                'symbol': data['symbol'],
                'direction': data['direction'],
                'entry_price': price
            })
        except Exception as e:
            logger.error(f"Inject error: {e}")
            return jsonify({'error': str(e)}), 500

    return app

