class CryptoSignalEngine:
    """Main orchestrator for the signal engine."""
    
    def __init__(self):
        self.trading_engine = PaperTradingEngine()
        self.data_fetcher = DataFetcher()
        self.signal_generator = SignalGenerator()
        self.signal_history = deque(maxlen=1000)  # Keep last 1000 signals
        self.bos_scan_log = deque(maxlen=5000)    # Keep last 5000 BOS scans
        self.last_signal_key = {}  # Dedup: strategy -> (direction, price_bucket)
        self.running = False
        self.flask_app = create_flask_app(self.trading_engine, self.signal_history, self.bos_scan_log)
    
    def run_cycle(self):
        """Single iteration of the main loop."""
        try:
            # Fetch latest data
            data = self.data_fetcher.fetch_all_symbols()
            
            if not data:
                logger.warning("PAPER MODE - No data fetched this cycle")
                return
            
            # Process each strategy
            for strategy_name, strategy_config in STRATEGIES_CONFIG.items():
                if not strategy_config['enabled']:
                    continue

                symbol = strategy_config['symbol']
                timeframe = strategy_config['timeframe']

                if symbol not in data:
                    continue

                df = data[symbol]

                # Log BOS scan + order flow for every cycle
                try:
                    swings = self.signal_generator.structure_analyzer.detect_swing_points(df, lookback=5)
                    bos_result = self.signal_generator.structure_analyzer.detect_bos_choch(swings, df)

                    scan_entry = {
                        'timestamp': datetime.now().isoformat(),
                        'symbol': symbol,
                        'timeframe': timeframe,
                        'strategy': strategy_name,
                        'price': float(df['close'].iloc[-1]),
                        'bos': bos_result.get('bos'),
                        'choch': bos_result.get('choch'),
                        'swing_count': len(swings),
                        'swing_highs': len([s for s in swings if s.type == 'high']),
                        'swing_lows': len([s for s in swings if s.type == 'low']),
                    }

                    # Attach order flow score to scan log
                    if self.signal_generator.of_scorer is not None:
                        try:
                            swing_dicts = [
                                {'index': s.timestamp, 'price': s.price, 'type': s.type}
                                for s in swings
                            ]
                            of_long = self.signal_generator.of_scorer.score(df, 'long', swing_dicts, 20)
                            of_short = self.signal_generator.of_scorer.score(df, 'short', swing_dicts, 20)
                            scan_entry['order_flow'] = {
                                'long_score': of_long.total_score,
                                'long_strength': of_long.strength.value,
                                'short_score': of_short.total_score,
                                'short_strength': of_short.strength.value,
                                'bias': of_long.direction_bias,
                                'data_type': 'OHLCV_PROXY',
                            }
                        except Exception as ofe:
                            scan_entry['order_flow'] = {'error': str(ofe)}

                    self.bos_scan_log.append(scan_entry)
                except Exception as e:
                    logger.warning(f"BOS log capture failed for {symbol}: {e}")

                # Generate signals
                signals = self.signal_generator.generate_signals(
                    symbol=symbol,
                    timeframe=timeframe,
                    df=df,
                    strategy_name=strategy_name
                )

                # Deduplicate: only take the best signal per strategy per cycle
                if signals:
                    best_signal = max(signals, key=lambda s: s.confidence_score)

                    # Check if this is truly new (different direction or price moved >0.5%)
                    sig_key = f"{strategy_name}_{best_signal.direction}"
                    price_bucket = round(best_signal.entry_price, 0)
                    last_key = self.last_signal_key.get(strategy_name)

                    if last_key and last_key == (best_signal.direction, price_bucket):
                        # Same signal as before — skip to avoid flooding
                        logger.debug(f"DEDUP: Skipping duplicate signal for {strategy_name}")
                    else:
                        self.last_signal_key[strategy_name] = (best_signal.direction, price_bucket)
                        self.signal_history.append(best_signal)
                        position = self.trading_engine.execute_signal(best_signal)

                        if position:
                            logger.info(f"PAPER MODE - Signal generated and executed: "
                                      f"{strategy_name} {best_signal.symbol} {best_signal.direction} "
                                      f"conf={best_signal.confidence_score:.1f}")
            
            # Update positions with current prices
            price_updates = {symbol: df['close'].iloc[-1] for symbol, df in data.items()}
            closed_positions = self.trading_engine.update_positions(price_updates)
            
            for pos in closed_positions:
                logger.info(f"PAPER MODE - Position closed: {pos.position_id}")
            
            # Save state
            self.trading_engine.save_positions()
            self.trading_engine.save_performance()
            
            logger.info(f"PAPER MODE - Cycle complete. Equity: ${self.trading_engine.current_equity:.2f}")
            
        except Exception as e:
            logger.error(f"PAPER MODE - Error in main cycle: {e}", exc_info=True)
    
    def run_main_loop(self):
        """Main event loop."""
        logger.info("PAPER MODE - Signal engine starting...")
        self.running = True
        
        # Use weighted timing: 1h strategies every 60s, 4h strategies every 240s
        # For simplicity, we'll use 60s per cycle
        cycle_interval = 60
        
        while self.running:
            self.run_cycle()
            time.sleep(cycle_interval)
    
    def start_api_server(self):
        """Start Flask API in a separate thread."""
        def run_flask():
            logger.info("PAPER MODE - Flask API starting on port 8099")
            self.flask_app.run(host='0.0.0.0', port=8099, debug=False, threaded=True)
        
        flask_thread = threading.Thread(target=run_flask, daemon=True)
        flask_thread.start()
        logger.info("PAPER MODE - Flask API thread started")
    
    def start(self):
        """Start the signal engine."""
        logger.info("=" * 80)
        logger.info("PAPER MODE - GodsView Crypto Signal Engine Starting")
        logger.info(f"Starting Capital: ${TRADING_CONFIG['starting_capital']:,.2f}")
        logger.info(f"Mode: {TRADING_CONFIG['mode']}")
        logger.info("=" * 80)
        
        # Start API server
        self.start_api_server()
        
        # Run main loop
        try:
            self.run_main_loop()
        except KeyboardInterrupt:
            logger.info("PAPER MODE - Shutdown signal received")
            self.stop()
    
    def stop(self):
        """Stop the signal engine."""
        self.running = False
        logger.info("PAPER MODE - Signal engine stopped")

