class MarketStructureAnalyzer:
    """Detects market structure: swings, order blocks, and flow."""
    
    def __init__(self):
        pass
    
    def detect_swing_points(
        self, 
        df: pd.DataFrame, 
        lookback: int = 10
    ) -> List[Swing]:
        """
        Detect swing high and swing low points.
        
        A swing high is a candle higher than 'lookback' candles before and after.
        A swing low is a candle lower than 'lookback' candles before and after.
        """
        swings = []
        df = df.copy()
        
        if len(df) < lookback * 2:
            return swings
        
        highs = df['high'].values
        lows = df['low'].values
        close = df['close'].values
        
        for i in range(lookback, len(df) - lookback):
            # Swing high
            if highs[i] == max(highs[i-lookback:i+lookback+1]):
                swings.append(Swing(
                    timestamp=int(df.index[i].timestamp()),
                    price=float(highs[i]),
                    type='high'
                ))
            
            # Swing low
            if lows[i] == min(lows[i-lookback:i+lookback+1]):
                swings.append(Swing(
                    timestamp=int(df.index[i].timestamp()),
                    price=float(lows[i]),
                    type='low'
                ))
        
        return sorted(swings, key=lambda x: x.timestamp)
    
    def detect_bos_choch(self, swings: List[Swing]) -> Dict[str, Any]:
        """
        Detect Break of Structure (BOS) and Change of Character (CHOCH).
        
        BOS: Price breaks above/below previous swing without changing direction
        CHOCH: Price breaks and changes from uptrend to downtrend (or vice versa)
        """
        if len(swings) < 3:
            return {'bos': None, 'choch': None}
        
        result = {'bos': None, 'choch': None}
        
        # Get last 3 significant swings
        recent_swings = swings[-3:]
        
        if len(recent_swings) == 3:
            s1, s2, s3 = recent_swings
            
            # Determine trend
            if s1.type == 'low' and s2.type == 'high' and s3.type == 'low':
                # Downtrend: low -> high -> lower low = BOS
                if s3.price < s1.price:
                    result['bos'] = {'direction': 'down', 'level': s1.price}
                    result['choch'] = {'direction': 'down', 'level': s3.price}
            
            elif s1.type == 'high' and s2.type == 'low' and s3.type == 'high':
                # Uptrend: high -> low -> higher high = BOS
                if s3.price > s1.price:
                    result['bos'] = {'direction': 'up', 'level': s1.price}
                    result['choch'] = {'direction': 'up', 'level': s3.price}
        
        return result
    
    def detect_order_blocks(self, df: pd.DataFrame) -> List[OrderBlock]:
        """
        Detect order blocks (accumulation/distribution zones).
        
        Bullish OB: Sharp down movement followed by consolidation
        Bearish OB: Sharp up movement followed by consolidation
        
        Strength scored 0-100 based on:
        - Size of initial impulse
        - Consolidation time
        - Proximity to current price
        """
        order_blocks = []
        df = df.copy()
        
        if len(df) < 20:
            return order_blocks
        
        closes = df['close'].values
        highs = df['high'].values
        lows = df['low'].values
        
        for i in range(10, len(df) - 5):
            # Bullish OB: Look for down impulse followed by consolidation
            if i >= 20:
                # Check if we had a sharp down move
                down_move = closes[i-10:i-5]
                current_zone = closes[i-5:i]
                
                if np.mean(down_move) > np.mean(current_zone):
                    down_pct = (np.mean(down_move) - np.mean(current_zone)) / np.mean(down_move)
                    if down_pct > 0.01:  # At least 1% move
                        strength = min(100, down_pct * 1000 + 50)
                        order_blocks.append(OrderBlock(
                            start_idx=i-10,
                            end_idx=i,
                            high=float(np.max(current_zone)),
                            low=float(np.min(current_zone)),
                            type='bullish',
                            strength=float(strength)
                        ))
            
            # Bearish OB: Look for up impulse followed by consolidation
            if i >= 20:
                up_move = closes[i-10:i-5]
                current_zone = closes[i-5:i]
                
                if np.mean(up_move) < np.mean(current_zone):
                    up_pct = (np.mean(current_zone) - np.mean(up_move)) / np.mean(up_move)
                    if up_pct > 0.01:  # At least 1% move
                        strength = min(100, up_pct * 1000 + 50)
                        order_blocks.append(OrderBlock(
                            start_idx=i-10,
                            end_idx=i,
                            high=float(np.max(current_zone)),
                            low=float(np.min(current_zone)),
                            type='bearish',
                            strength=float(strength)
                        ))
        
        return order_blocks
    
    def detect_liquidity_sweeps(
        self, 
        df: pd.DataFrame, 
        swings: List[Swing]
    ) -> Dict[str, Any]:
        """
        Detect liquidity sweeps (stops are hit, then reversals).
        """
        if len(swings) < 2:
            return {'sweeps': []}
        
        sweeps = []
        lows = df['low'].values
        highs = df['high'].values
        
        for i in range(1, len(swings)):
            prev_swing = swings[i-1]
            curr_swing = swings[i]
            
            if prev_swing.type == 'low' and curr_swing.type == 'high':
                # Check if price went below prev low
                min_price_since = np.min(lows[max(0, i-5):i+1])
                if min_price_since < prev_swing.price:
                    sweeps.append({
                        'level': prev_swing.price,
                        'type': 'liquidity_below',
                        'touched_at': datetime.now()
                    })
            
            elif prev_swing.type == 'high' and curr_swing.type == 'low':
                # Check if price went above prev high
                max_price_since = np.max(highs[max(0, i-5):i+1])
                if max_price_since > prev_swing.price:
                    sweeps.append({
                        'level': prev_swing.price,
                        'type': 'liquidity_above',
                        'touched_at': datetime.now()
                    })
        
        return {'sweeps': sweeps}

