class DataFetcher:
    """Fetches OHLCV data from exchange via ccxt."""
    
    def __init__(self, exchange_name: str = 'kraken'):
        self.exchange_name = exchange_name
        self.exchange = getattr(ccxt, exchange_name)()
        self.last_fetch_time = defaultdict(float)
    
    def fetch_ohlcv(
        self,
        symbol: str,
        timeframe: str,
        limit: int = 100
    ) -> Optional[pd.DataFrame]:
        """
        Fetch OHLCV data from exchange.
        
        Returns DataFrame or None if error
        """
        try:
            # Rate limit: minimum 5 seconds between fetches per symbol
            key = f"{symbol}_{timeframe}"
            time_since_last = time.time() - self.last_fetch_time[key]
            if time_since_last < 5:
                time.sleep(5 - time_since_last)
            
            candles = self.exchange.fetch_ohlcv(symbol, timeframe, limit=limit)
            
            df = pd.DataFrame(
                candles,
                columns=['timestamp', 'open', 'high', 'low', 'close', 'volume']
            )
            df['timestamp'] = pd.to_datetime(df['timestamp'], unit='ms')
            df.set_index('timestamp', inplace=True)
            
            self.last_fetch_time[key] = time.time()
            return df
            
        except Exception as e:
            logger.error(f"PAPER MODE - Error fetching {symbol} {timeframe}: {e}")
            return None
    
    def fetch_all_symbols(self) -> Dict[str, pd.DataFrame]:
        """Fetch data for all configured symbols."""
        data = {}
        
        for strategy, config in STRATEGIES_CONFIG.items():
            if not config['enabled']:
                continue
            
            symbol = config['symbol']
            timeframe = config['timeframe']
            
            df = self.fetch_ohlcv(symbol, timeframe, limit=config['lookback_candles'])
            if df is not None:
                data[symbol] = df
            else:
                logger.warning(f"PAPER MODE - Failed to fetch {symbol}")
        
        return data

